#include "libft.h"

void	*ft_memset(void *ptr, int x, size_t n)
{
	char	*str;
	size_t	i;

	str = (char*)ptr;
	i = 0;
	while (i < n)
	{
		str[i] = x;
		i++;
	}
	return (str);
}
